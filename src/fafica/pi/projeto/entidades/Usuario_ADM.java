package fafica.pi.projeto.entidades;

public class Usuario_ADM {
	private Integer id_UsuarioAdm;
	private String nome;
	private String nomesalao;
	private String endereco;
	private String telefone;
	private String login;
	private String senha;
	
	public Integer getId_UsuarioAdm() {
		return id_UsuarioAdm;
	}
	public void setId_UsuarioAdm(Integer id_UsuarioAdm) {
		this.id_UsuarioAdm = id_UsuarioAdm;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getNomeSalao() {
		return nomesalao;
	}
	public void setNomeSalao(String nomeSalao) {
		this.nomesalao = nomeSalao;
	}
	public String getEndereco() {
		return endereco;
	}
	public void setEndereco(String endereco) {
		this.endereco = endereco;
	}
	public String getTelefone() {
		return telefone;
	}
	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}
	public String getLogin() {
		return login;
	}
	public void setLogin(String login) {
		this.login = login;
	}
	public String getSenha() {
		return senha;
	}
	public void setSenha(String senha) {
		this.senha = senha;
	}
	
	

}
